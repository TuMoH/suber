package com.timursoft.suber;

public class SuberParseException extends Exception {

    public SuberParseException() {
        super();
    }

    public SuberParseException(String message) {
        super(message);
    }

    public SuberParseException(String message, Throwable cause) {
        super(message, cause);
    }

    public SuberParseException(Throwable cause) {
        super(cause);
    }

}
